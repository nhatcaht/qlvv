    public function slug($str = '')
        {
            $str = $this->removeSign($str);
            $str = strtolower($str);
            $str = preg_replace('/[^a-z0-9 ]+/i', '', $str);
            $str = preg_replace('/[[:blank:]]+/', '-', $str);
            return $str;
        }
